            <h4 class="judul">Sejarah</h4>
            <p><span>Sunda Kelapa</span> (397-1527) Jakarta pertama kali dikenal sebagai salah satu pelabuhan Kerajaan
                Sunda yang bernama Sunda Kalapa berlokasi di muara Sungai Ciliwung. Ibu kota Kerajaan Sunda yang dikenal
                sebagai Dayeuh Pakuan Pajajaran atau Pajajaran (sekarang Bogor) dapat ditempuh dari pelabuhan Sunda
                Kalapa selama dua hari perjalanan. Menurut sumber Portugis, Sunda Kalapa merupakan salah satu pelabuhan
                yang dimiliki Kerajaan Sunda selain pelabuhan Banten, Pontang, Cigede, Tamgara dan Cimanuk. Kerajaan
                Sunda sendiri merupakan kelanjutan dari Kerajaan Tarumanagara pada abad ke-5 sehingga pelabuhan ini
                diperkirakan telah ada sejak abad ke-5 dan diperkirakan merupakan ibu kota Tarumanagara yang disebut
                Sundapura. Pada abad ke-12, pelabuhan ini dikenal sebagai pelabuhan lada yang sibuk. Kapal-kapal asing
                yang berasal dari Tiongkok, Jepang, India Selatan, dan Timur Tengah sudah berlabuh di pelabuhan ini
                membawa barang-barang seperti porselen, kopi, sutra, kain, wangi-wangian, kuda, anggur, dan zat warna
                untuk ditukar dengan rempah-rempah yang menjadi komoditas dagang saat itu.</p>
            <br>
            <br>
            <p><span>Jayakarta</span> (1527-1619) Bangsa Portugis merupakan Bangsa Eropa pertama yang datang ke Jakarta.
                Pada abad ke-16, Surawisesa, raja Sunda meminta bantuan Portugis yang ada di Malaka untuk mendirikan
                benteng di Sunda Kelapa sebagai perlindungan dari kemungkinan serangan Cirebon yang akan memisahkan diri
                dari Kerajaan Sunda. Upaya permintaan bantuan Surawisesa kepada Portugis di Malaka tersebut diabadikan
                oleh orang Sunda dalam cerita pantun seloka Mundinglaya Dikusumah. Surawisesa diselokakan dengan nama
                gelarnya yaitu Mundinglaya. Namun sebelum pendirian benteng tersebut terlaksana, Cirebon yang dibantu
                Demak langsung menyerang pelabuhan tersebut. Orang Sunda menyebut peristiwa ini tragedi, karena
                penyerangan tersebut menghanguskan kota pelabuhan tersebut dan membunuh banyak rakyat Sunda di sana
                termasuk syahbandar pelabuhan. Penetapan hari jadi Jakarta pada 22 Juni oleh Wali Kota Jakarta Sudiro
                pada 1956 berdasarkan tragedi pendudukan pelabuhan Sunda Kalapa oleh Fatahillah pada 1527. Fatahillah
                mengganti nama kota tersebut menjadi Jayakarta yang berarti kota kemenangan. Selanjutnya Sunan Gunung
                Jati dari Kesultanan Cirebon menyerahkan pemerintahan di Jayakarta kepada putranya yaitu Maulana
                Hasanuddin dari Banten yang menjadi sultan di Kesultanan Banten.</p>
            <br>
            <br>
            <p><span>Batavia </span>(1619-1942) Orang Belanda datang ke Jayakarta sekitar akhir abad ke-16, setelah
                singgah di Banten pada 1596. Jayakarta pada awal abad ke-17 diperintah oleh Pangeran Jayakarta, salah
                seorang kerabat Kesultanan Banten. Pada 1619, VOC dipimpin oleh Jan Pieterszoon Coen menduduki Jayakarta
                setelah mengalahkan pasukan Kesultanan Banten dan kemudian mengubah namanya menjadi Batavia. Selama
                kolonialisasi Belanda, Batavia berkembang menjadi kota yang besar dan penting. Untuk pembangunan kota,
                Belanda banyak mengimpor budak-budak sebagai pekerja. Kebanyakan dari mereka berasal dari Bali,
                Sulawesi, Maluku, Tiongkok, dan pesisir Malabar, India. Pada 9 Oktober 1740, terjadi kerusuhan di
                Batavia dengan terbunuhnya 5.000 orang Tionghoa. Dengan terjadinya kerusuhan ini, banyak orang Tionghoa
                yang lari ke luar kota dan melakukan perlawanan terhadap Belanda. Dengan selesainya Koningsplein
                (Gambir) pada 1818, Batavia berkembang ke arah selatan. Pada 1 April 1905 di Ibu Kota Batavia dibentuk
                dua kotapraja atau gemeente, yakni Gemeente Batavia dan Meester Cornelis. Pada 1920, Belanda membangun
                kota taman Menteng. Wilayah ini menjadi tempat baru bagi petinggi Belanda menggantikan Molenvliet di
                utara. Pada 1935, Batavia dan Meester Cornelis (Jatinegara) terintegrasi menjadi wilayah Jakarta Raya.
                Pada 1 Januari 1926 pemerintah Hindia Belanda mengeluarkan peraturan untuk pembaharuan sistem
                desentralisasi dan dekonsentrasi yang lebih luas. Di Pulau Jawa dibentuk pemerintahan otonom provinsi.
            </p>
            <br>
            <br>
            <p><span>Jakarta</span> (1942-sekarang) Penjajahan oleh Jepang dimulai pada 1942 dan mengganti nama Batavia
                menjadi Djakarta untuk menarik hati penduduk pada Perang Dunia II. Kota ini juga merupakan tempat
                dilangsungkannya Proklamasi Kemerdekaan Republik Indonesia pada 17 Agustus 1945 dan diduduki Belanda
                sampai pengakuan kedaulatan pada 1949. Nama Jakarta kembali dikukuhkan pada 22 Juni 1956 di masa
                pemerintahan Wali Kota Jakarta Sudiro (1952-1960). Sebelum 1959, Djakarta merupakan bagian dari Provinsi
                Jawa Barat. Pada 1959, status Kota Djakarta mengalami perubahan dari sebuah kotapraja di bawah wali kota
                ditingkatkan menjadi daerah tingkat satu (Dati I) yang dipimpin oleh gubernur. Pada 1961, status Jakarta
                diubah dari Daerah Tingkat Satu menjadi Daerah Khusus Ibu Kota (DKI). Penetapan 22 Juni sebagai hari
                ulang tahun Jakarta didasari momen Fatahillah berhasil mengusir Portugis dari Sunda Kelapa pada 22 Juni
                1527. Hal ini tertuang dalam keputusan DPR kota sementara No. 6/D/K/1956. Pemerintahan Daerah Khusus Ibu
                Kota Jakarta ditetapkan melalui PP No 2 Tahun 1961 juncto UU No 2 PNPS 1961. (OL-14)</p>
